export const getByIsoCode2 = (code2: string) => {
    return COUNTRIES_DATA.find(country => country.isoCode2 === code2);
}

export const getByCountryCode = (code: string) => {
    return COUNTRIES_DATA.find(country => country.countryCodes.includes(code));
}

export const getByCountryName = (name: string) => {
    return COUNTRIES_DATA.find(country => country.country === name);
}

export const getByCountryArabicName = (name: string) => {
    return COUNTRIES_DATA.find(country => country.arabicName === name);
}

export const searchByCountryName = (name: string) => {
    name = name.toLowerCase();
    return COUNTRIES_DATA.filter(country => country.country.toLowerCase().includes(name));
}

export const searchByCountryArabicName = (name: string) => {
    name = name.toLowerCase();
    return COUNTRIES_DATA.filter(country => country.arabicName.toLowerCase().includes(name));
}

export const searchCountryData = (nameOrCode: string) => {
    nameOrCode = nameOrCode.toLowerCase();
    return COUNTRIES_DATA.filter(country =>
        country.countryCodes.includes(nameOrCode)
        || country.country.toLowerCase().includes(nameOrCode)
        || country.arabicName.toLowerCase().includes(nameOrCode));
}
